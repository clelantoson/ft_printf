#include "ft_printf.h"

int		ft_is_in_type_list(int c)
{
	return ((c == 'c') || (c == 's') || (c == 'd') || (c == 'i') || (c == 'u')
	|| (c == 'x') || (c == 'X') || (c == 'p') || (c == '%'));
}
